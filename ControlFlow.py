#Lesson 2 Control Flow

#If Else Statement
x = 5
if x < 10 : print(True)

if x > 5 : print(True)
else : print(False)

#Elif Statement
x = 10
if x < 10:
    print("x is less than 10")
elif x > 10:
    print("x is more than 10")
else: 
    print ("x is 10")

#While Statement
n=0
while n < 3:
    print(n)
    n=n+1

